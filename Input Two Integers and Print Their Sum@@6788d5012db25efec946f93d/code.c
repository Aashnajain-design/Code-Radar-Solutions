#include<stdio.h>

int main()
{
    int a, b, sum;  // Declare variables for input and sum
    printf("%d", a);
    scanf("%d", &a);  // Take input for 'a'
    
    printf("%d", b);
    scanf("%d", &b);  // Take input for 'b'
    
    sum = a + b;  // Calculate the sum of a and b
    printf("Sum: %d", sum);  // Print the sum
    
    return 0;
}